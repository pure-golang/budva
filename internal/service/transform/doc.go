// Package transform применяет текстовые трансформации к сообщениям.
//
// Использование:
//
//	svc := transform.New(translator, logger)
//	text, err := svc.Apply(ctx, text, source, destination)
//
// Пакет не читает переменные окружения напрямую.
//
// Ограничения:
//
//   - Трансформации применяются последовательно: перевод, замена фрагментов,
//     добавление подписи, ссылки на источник, навигации prev/next.
//   - Зависит от переводчика через частично применяемый интерфейс translator.
package transform
