// Package queue реализует in-process FIFO-очередь задач.
//
// Использование:
//
//	q := queue.New()
//	q.StartContext(ctx)
//	defer q.Close()
//
//	q.Add(func() { /* задача */ })
//
// Для синхронного выполнения в тестах:
//
//	q.ProcessAll()    // все задачи, включая добавленные во время выполнения
//	q.ProcessBatch()  // только текущие задачи
//
// Пакет не читает переменные окружения напрямую.
//
// Ограничения:
//
//   - Очередь обрабатывает одну задачу в секунду.
//   - Задачи выполняются последовательно в одной горутине.
//   - Паника внутри задачи перехватывается и логируется.
//   - Потокобезопасен для Add и Len.
package queue
